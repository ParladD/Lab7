

function backgroundText(x){

    var y = document.getElementById(x);

    y.style.backgroundColor= "#06F0FF";
    y.style.fontStyle = "italic"

}

function backgroundNONE(x){

    var y = document.getElementById(x);

    y.style.backgroundColor= null;

}

function itaclics(x) {
    var y = document.getElementById(x);
    y.style.fontStyle = "italic";

}
